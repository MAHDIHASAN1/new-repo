###নিয়ম কানুন_GIT

#* সমস্ত লেখাকে পরিষ্কার করার জন্য                              ("clear")
#* নতুন কোন ফোল্ডার তৈরি করতে                               ("mkdir"ফোল্ডারের নাম)
#* কোন ফোল্ডারকে মুছে ফেলার জন্য                             ("rmdir"ফোল্ডারের নাম) 
#* এই ফোল্ডারে কি রয়েছে তা দেখার জন্য                         ("ls" / "dir")
#* কোন ফোল্ডারে প্রবেশ করার জন্য                              ("cd" ফোল্ডারের নাম)
#* পিছনে যাওয়ার জন্য                                         ("cd ..")
#* কোন টেক্সট বা পাইথন ফাইল তৈরি করার জন্য                   ("touch"ফোল্ডারের নাম)
#* কোন ফাইলকে গিটের ভিতরে নিতে                             ("git init")
#* কোন কোন ফাইলে পরিবর্তন করা হয়েছে তা জানার জন্য           ("git status") 
#* আমি কোন ফাইলে আছি তা জানার জন্য                        ("pwd")
#* সব কিছুকে স্টেজে নেওয়ার জন্য                               ("git add --all") 
#* এই ফোল্ডারে যা আছে সব স্টেজে উঠানোর জন্য                  ("git add.") 
#* একটি ফোল্ডারকে স্টেজে উঠানোর জন্য                          ("git add "ফোল্ডারের নাম")        
#* ডিলিট ফোল্ডার ব্যতীত বাকি সব গুলোকে স্টেজে তোলার জন্য       ("git add *")
#* সমস্তটি txt ফাইল গুলোকে স্টেজে ওঠার জন্য                   ("git add *.txt")
#* স্টেজ থেকে আবার লোকালে নেওয়ার জন্য                       ("git reset") 
#* কোন ফোল্ডারকে স্টেজ থেকে কমিট-এ নেওয়ার জন্য              ("git commit -m "I have made some changes to files"")
#* কমিট থেকে আবার স্টেজে নেওয়ার জন্য                         ("git reset HEAD~")
#* মুছে ফেলা ফাইলকে আবার ফিরিয়ে আনার জন্য                   ("git reset --hard") 
#* কোন ফাইলকে রিমুভ করে একবারে স্টেজে উঠানোর জন্য           ("git rm"ফাইলের নাম")  
#* কিছু পরিবর্তন করার পর রিমুভ করার জন্য                       ("git rm"ফাইলের নাম -f") 
#* পরিবর্তন করার পর লোকাল রেখে স্টেজে উঠিয়ে রিমুভ করার জন্য   ("git rm --cached"ফাইলের নাম")
#* অ্যাপ্লিকেশনে কয়টি Branch আছে তা জানার জন্য                ("git branch")
#* নতুনBranch তৈরি করার জন্য                                 ("git branch"ফাইলের নাম")
#* এক Branchথেকে অন্য Branch যাওয়ার জন্য                    ("git checkout"ফাইলের নাম") 
#* কোন Branch কে Merge  করার জন্য                          ("git merge"Branchএর নাম-m"merging on "Branchএর নাম" with "Branchএর নাম"") 
#* লোকাল থেকে রিমোটে নেওয়ার জন্য                           ("git push origin"Branchএর নাম") 
#* শুধু এই সফটওয়্যার এর জন্য User Name ব্যবহার করা করার জন্য  ("git config user.name"User Name" ")             
#* শুধু এই সফটওয়্যার এর জন্য GMAIL ব্যবহার করা করার জন্য      ("git config user.email"GMAIL" ")               
#* সমস্ত সফটওয়্যার এর জন্য User Name  ব্যবহার করার জন্য       ("git config --global user.name"User Name" ")   
#* সমস্ত সফটওয়্যার এর জন্য GMAIL ব্যবহার করার জন্য            ("git config --global user.email"GMAIL" ")          
#* git-এ বর্তমানে কোন User Name টি দেওয়া আছে               ("git config --list")                                
