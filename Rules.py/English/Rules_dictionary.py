###Rules_dictionary

#* Only to get the keys in the dictionary                                         {print(Dictionary Name.keys())}            
#* To get the values in the dictionary only                                       {print(Dictionary Name.values())}  
#* To add an item to a dictionary                                                 {Dictionary Name["Keys"] = "Values"}
#* To delete an item from the dictionary                                          {del Dictionary Name["Keys"]}/........................................................./{Dictionary Name.pop("Keys")}        
#* To do two dictionaries together                                                {That will be kept inside.update(Which will be added)}/................................/{That will be kept inside |= Which will be added}--------------(Note: If any keys are the same then the keys of the one to be attached will work) 
#* To create a new dictionary without any changes to the original dictionary      {New Dictionary Name = {**That will be kept inside,**Which will be added}}/............/{print(That will be kept inside | Which will be added)}   
