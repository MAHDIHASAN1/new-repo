###Rules_git

#* To clear all writing                                         ("clear"The name of the folder)
#* To create a new folder                                       ("mkdir"The name of the folder)
#* To delete a folder                                           ("rmdir"The name of the folder) 
#* To see what's in this folder                                 ("ls" / "dir") 
#* To access the folder                                         ("cd" The name of the folder)
#* To go back                                                   ("cd ..")
#* To create a text or Python file                              ("touch"The name of the folder)
#* To take a file inside the git                                ("git init") 
#* To find out which files have been modified                   ("git status")
#* To find out which file I'm on                                ("pwd") 
#* To take everything on stage                                  ("git add --all"/"git add -A") 
#* Everything in this folder is up for stage                    ("git add.")
#* To lift a folder to the stage                                ("git add "The name of the folder")     
#* To take everything on stage except the delete folder         ("git add *")
#* All txt files to get on stage                                ("git add *.txt")                              
#* From Stage to Local again                                    ("git reset") 
#* To take a folder from the stage to Commit                    ("git commit -m ""some writing")
#* From Commit to Stage take again                              ("git reset HEAD~") 
#* To restore deleted files                                     ("git reset --hard") 
#* To remove a file and take it to the stage at once            ("git rm"files name") 
#* To remove after making some changes                          ("git rm"files name -f")  
#* After changing,to leave the local and remove it on the stage ("git rm --cached"files name)
#* To know how many branches there are in the application       ("git branch")
#* To create a new branch                                       ("git branch"files name")
#* To go from one branch to another                             ("git checkout"files name") 
#* To Merge a Branch                                            ("git merge"Branch name -m"merging on "Branch name" with "Branch name"") 
#* To take from local to remote                                 ("git push origin"Branch name") 
#* Just to use the User Name for this software                  ("git config user.name"User Name" ")         
#* Just to use the GMAIL for this software                      ("git config user.email"GMAIL" ")                
#* To use Gmail for all software                                ("git config --global user.name"User Name" ")      
#* To use User Name for all software                            ("git config --global user.email"GMAIL" ")       
#* git-এ বর্তমানে কোন User Name টি দেওয়া আছে                    ("git config --list")                                                           
