####Rules_PYTHON

#* For new lines                                                  {Writing\n Writing}
#* To use a tap between two words                                 {Writing\t Writing}
#* To print in one line                                           {print("Writing" /......................................................./ 'Writing')}
#* To print in many lines                                         {print(""" Writing """)}
#* To block a message by a single quotation                       {print(" Writing 'message' "/............................................/print(' Writing \'message\' ')}
#* To block a message by a double quotation                       {print(' Writing "message" '/............................................/print(" Writing \"message\" ")}
#* To comment on anything                                         {''' '''(To do in many lines) /........................................../ # Writing(To do in one line)}
#* To find out how many characters are inside a string            {print(len("Writing"))}
#* ইংরেজি সকল ছোট অক্ষর গুলো বড় অক্ষরে লেখার জন্য                {print("Writing".upper())}
#* English to make all lowercase letters uppercase                {print("Writing".lower())}
#* লেখার শুরুতে অথবা শেষে যদি কোন স্পেস থাকে তাকে বন্ধ করার জন্য   {print("   Writing    ".strip())}
#* To find out where this character is inside the string          {print("Writing".find("S"))}
#* If one word is replaced by another                             {print("Writing Writing".replace("Writing","Writing"))}
#* Find out how much code is used for the string                  {print(dir("Writing"))}





